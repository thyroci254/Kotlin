fun main(){
    // arithmetic operators
    // addition
    val num1: Int = 100
    val num2: Int = 50
    val num3: Int = 40
    val addition: Int = num1+num2+num3
    val subtraction: Int = num1-num2
    val product: Int = num1*num2*num3
