fun main(){
    // Addition of two numbers
    fun addTwo(){
        val num1: Int = 40
        val num2: Int = 60
        val sum: Int = num1+num2
        println("The addition is $sum")
    }
    // call/Invoke your function
    addTwo()
    // Subtraction of two 
    fun subTwo(){
        val num3: Int = 40
        val num4: Int = 20
        val sub: Int = num3-num4
        println("The subtraction is $sub")
    }
    subTwo()
    
    // Division of  Two Numbers 
    fun divTwo(){
        val num3: Int = 40
        val num4: Int = 20
        val div: Int = num3/num4
        println("The Division is $div")
    }
    divTwo()

    // Product of  Two Numbers 
    fun productTwo(){
        val num3: Int = 40
        val num4: Int = 20
        val product: Int = num3*num4
        println("The Product is $product")
    }
    productTwo()
}